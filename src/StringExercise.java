public class StringExercise {
    public static void main(String[] args) {

        String pink = "We don't need no Education";
        System.out.println(pink);
        String floyd = "We don't need no thought control";
        System.out.println(floyd);

        String output = "We don't need no education\nWe don't need no thought control";
        System.out.println(output);
//        does the same think as the first two

        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(check);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);

    }
}
