package PearsonPractice.Chapter4;

public class MathDemo {
    public static void main(String[] args) {

        System.out.println(Math.cos(Math.PI / 6));
        System.out.println(Math.toRadians(30));
        System.out.println(Math.toDegrees(0.52));
        System.out.println(Math.sin(Math.PI /6));

        System.out.println(Math.exp(1));
        System.out.println(Math.log(Math.E));
        System.out.println(Math.pow(2,2));
        System.out.println(Math.sqrt(25));

        System.out.println(Math.ceil(12.2));
        System.out.println(Math.floor(12.2));
        System.out.println(Math.rint(15.8));
        System.out.println(Math.rint(14.5));
        System.out.println(Math.round(14.5));

        System.out.println(Math.max(2, 5));
        System.out.println(Math.min(2, 5));
        System.out.println(Math.abs(-42.5));

       System.out.println((int) (1 + Math.random()*10));


    }
}
